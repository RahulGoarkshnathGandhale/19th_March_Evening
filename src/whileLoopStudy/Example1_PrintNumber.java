package whileLoopStudy;

public class Example1_PrintNumber   //To print the number from 1 to 10 using while loop 
{

	public static void main(String[] args)
	{
		int i=1;                 //Start condition
		
		while(i<=10)             //end condition
		{
			System.out.println(i);
			
			i++;                //increament/decreament
		}
	}

}
