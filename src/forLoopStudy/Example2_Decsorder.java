package forLoopStudy;

public class Example2_Decsorder   //To print the number between 1 to 10 in desc order
{

	public static void main(String[] args)
	{
		
		System.out.println("To print the number between 1 to 10 in desc order");
		
		for(int j=10; j>=1; j--)   //10  10>=1 T 9
		{
			System.out.println(j);
		}
	}

}
