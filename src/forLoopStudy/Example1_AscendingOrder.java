package forLoopStudy;

public class Example1_AscendingOrder   //To print the number between 1 to 50 in ascending order
{

	public static void main(String[] args) 
	{
		System.out.println("The number between 1 to  50 in ascending order is");
		for(int i=1; i<=50; i++)
		{
			
			System.out.println(i);
		}
		
		
	}

}
