package methodWithOutParameter;

public class StaticMethodStudy {   //call static method from same class

	public static void main(String[] args) 
	{
		myMethod1();
		myMethod2();
		

	}
	
	public static void myMethod1()
	{
		System.out.println("This is static myMethod1 ");
	}
	
	public static void myMethod2()
	{
		System.out.println("This is static myMethod2");
	}

}
