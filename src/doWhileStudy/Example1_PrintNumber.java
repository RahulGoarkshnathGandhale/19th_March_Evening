package doWhileStudy;

public class Example1_PrintNumber  //to print the number by using Do While loop
{

	public static void main(String[] args) 
	{
	
		int i=1;                //start condition
		do 
		{
			System.out.println(i);
			i++;                      //increament or decreament
		}
		
		while(i<=10);             //end condition

	}

}
